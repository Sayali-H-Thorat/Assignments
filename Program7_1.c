//Problem Statement: Write a program which accept number from user and print the number of $ and * on screen
//
//Input:  5
//Output: $  *   $   *   $   *   $   *   $   *
//
//Input:  3
//Output: $  *   $   *   $   *      
//
//Input:  -3
//Output: $  *   $   *   $   *   
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Function Name:     Pattern()
// Description :      Accept number from user and print that numbers of $ and *
// Input :            Integer
// Output :           $ *(Symbols)
//Time Complexity:    N
// Author :           Sayali Hanumant Thorat
// Date :             03/11/2022
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf(" $ * ");
    }
}
int main()
{
    int iValue = 0;

    printf("Enter the number:\n");
    scanf("%d", &iValue);

    Pattern(iValue); 

    return 0;
}