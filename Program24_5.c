//Problem Statement: Accept character from user and display its ASCII values in decimal, octal and hexadecimal format.
//
//Input: A
//Output: Decimal: 65
//        Octal : 0101
//        Hexadecimal: 0x41
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Function Name:     Display()
// Description :      Display decimal octal and hexadecimal values of character
// Input :            character
// Output :           Integer(decimal,octal,hexadecimal)
// Author :           Sayali Hanumant Thorat
// Date :             17/11/2022
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    printf("Ascii value of %c character in decimal is : %d\n", ch,ch);
    printf("Ascii value of %c character in Octal is : %o\n", ch,ch);
    printf("Ascii value of %c character in Hexadecimal is : %x\n", ch,ch);

}

int main()
{
    char cValue = '\0';

    printf("Enter the character:\n");
    scanf("%c", &cValue);

    Display(cValue);

    return 0;
}