void struprX(char *str)
{
    while(*str != '\0')
    {
        if((*str >= 'a') && (*str <= 'z'))
        {
             *str = *str - 32;

        }
        str++;
    }

}

int main()
{
    char arr[20];

    printf("Enter the String :\n");
    scanf("%[^'\n']s", arr);

    printf("Original String is: %s\n", arr);

    struprX(arr);
    
    printf("Modified String is: %s\n", arr);

    return 0;

}