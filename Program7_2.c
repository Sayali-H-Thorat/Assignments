#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Function Name:     Display()
// Description :      Accept number from user and print numbers till that accepted number
// Input :            Integer
// Output :           Integer(Numbers)
//Time Complexity:    N
// Author :           Sayali Hanumant Thorat
// Date :             03/11/2022
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt =0;

    for(iCnt =1; iCnt <= iNo; iCnt++)
    {
        printf("%d\t", iCnt);
    }
}
int main()
{
    int iValue = 0;

    printf("Enter the number :");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}