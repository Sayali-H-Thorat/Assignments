void strlwrX(char *str)
{
    while(*str != '\0')
    {
        if((*str >= 'A') && (*str <= 'Z'))
        {
             *str = *str + 32;

        }
        str++;
    }

}

int main()
{
    char arr[20];

    printf("Enter the String :\n");
    scanf("%[^'\n']s", arr);

    printf("Original String is: %s\n", arr);

    strlwrX(arr);

    printf("Modified String is: %s\n", arr);

    return 0;

}