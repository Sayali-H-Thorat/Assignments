int CountSmall(char *str)
{
    int iCnt = 0;

    if(*str == 0)
    {
        return 1;
    }

    while(*str != '\0')
    {
        if((*str >= 'a') && (*str <= 'z'))
        {
            iCnt++;
        }
        str++;
    }
    return iCnt;
}

int main()
{
    char arr[20];
    int iRet = 0;

    printf("Enter String:\n");
    scanf("%[^'\n']s", arr);

    iRet = CountSmall(arr);

    printf("Small Letters are : %d\n", iRet);

    return 0;
}