void StrRevX(char *str)
{
    char *start = str;
	char *end = str;
	char temp = '\0';
	
	while(*end != '\0')
	{
		
		end++;
	}
	end--;
	
	while(start < end)
	{
		temp = *start;
		*start = *end;
		*end = temp;
		
		start++;
		end--;
		
	}

    
}

int main()
{
    char arr[20];

    printf("Enter String:\n");
    scanf("%[^'\n']s", arr);

    StrRevX(arr);

    printf("Modified / Reverse string is : %s\n",arr);

    return 0;
}