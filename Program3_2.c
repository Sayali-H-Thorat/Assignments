#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Function Name: DisplayFactor()
// Description :  Accept one numbers from user and display the even factors that number
// Input :        Integer
// Output :       (Integer)Even factors
// Author :       Sayali Hanumant Thorat
