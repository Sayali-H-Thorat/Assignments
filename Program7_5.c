//Problem Statement: Write a program which accept N from user and print first 5 multiples of N
//
//Input:  4
//Output: 4  8   12  16  50
//
//Input:  3
//Output: 3  6   9   12  15     
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Function Name:     MultipleDisplay()
// Description :      Accept number from user and print first five multiples of that number
// Input :            Integer
// Output :           Integer(First five multiples of that number)
//Time Complexity:    N
// Author :           Sayali Hanumant Thorat
// Date :             03/11/2022
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void MultipleDisplay(int iNo)
{
    int iCnt = 0;
    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("%d\t", iNo*iCnt);
    }
}
int main()
{
    int iValue = 0;

    printf("Enter the number:\n");
    scanf("%d", &iValue);

    MultipleDisplay(iValue);

    return 0;
}